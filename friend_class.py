class Friend:
    def __init__(self, birthday, name, friend_type, notes, message_approval, language, phone,
                 email, comms_pref, meme_pref, theme):
        self.birthday = birthday
        self.name = name
        self.friend_type = friend_type
        self.notes = notes
        self.message_approval = message_approval
        self.language = language
        self.phone = phone
        self.email = email
        self.comms_pref = comms_pref
        self.meme_pref = meme_pref
        self.theme = theme
